export function findPlan(sku: string): Plan | undefined {
  return plans.find((p) => p.sku === sku);
}

export function plansByCategory(category: string): Plan[] {
  const lower = category.toLowerCase();
  return plans.filter(
    (p) =>
      p.category_l1.toLowerCase() === lower ||
      p.categories.toLowerCase().includes(lower),
  );
}

